package com.mkpatir.engelsizyasam.models;

public class IllnessModel {
    private String name_surname;
    private String birthdate;
    private String ill;
    private String sex;
    private String enter_exit_date;
    private String enter_or_exit;
    private String fromemployee;

    public String getName_surname() {
        return name_surname;
    }

    public void setName_surname(String name_surname) {
        this.name_surname = name_surname;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getIll() {
        return ill;
    }

    public void setIll(String ill) {
        this.ill = ill;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getEnter_exit_date() {
        return enter_exit_date;
    }

    public void setEnter_exit_date(String enter_exit_date) {
        this.enter_exit_date = enter_exit_date;
    }

    public String getEnter_or_exit() {
        return enter_or_exit;
    }

    public void setEnter_or_exit(String enter_or_exit) {
        this.enter_or_exit = enter_or_exit;
    }

    public String getFromemployee() {
        return fromemployee;
    }

    public void setFromemployee(String fromemployee) {
        this.fromemployee = fromemployee;
    }
}
